package JavaBasics;

public class StringConcatenation {

	public static void main(String[] args) {
	   int a = 100;
	   int b = 200;
	   
	   System.out.println(a+b);
	   
	   String x = "Harsha";
	   String y = "Reddy";
	   System.out.println(x+y);
	   
	   System.out.println(a+b+x+y);
	   System.out.println(x+y+a+b);
	   System.out.println(x+y+(a+b));
	}

}
