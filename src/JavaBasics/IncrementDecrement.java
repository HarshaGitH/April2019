package JavaBasics;

public class IncrementDecrement {

	public static void main(String[] args) {
		int i =1;
		int j = i++;
		
		System.out.println(j);
		
		int a = 1;
		int b = ++a;
		System.out.println(b);
		
		int m = 2;
		int n = m--;
		System.out.println(n);
		
		int k =3;
		int l =--k;
		System.out.println(l);
			

	}

}
