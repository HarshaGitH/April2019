package JavaBasics;

public class WhileLoops {

	public static void main(String[] args) {
		
		int i = 1;
		while(i<=10) {
			System.out.print(i);
			i=i+1;
		}

		//For Loop
		
		for(int j =1;j<=10;j++) {
			System.out.println(j);
			}
		
		for(int k =10;k>=1;k--) {
			System.out.println(k);
		}
	}

}
