package JavaBasics;

public class DataTypes {

	public static void main(String args[]) {
		int i = 10;
		//Cannot declare int i again but can be replaced  i=20
		
		//Double will store decimal values
		double d = 12.33;
		
		//Char can store single characters
		char c = 'A';
		
		//Boolean is true or false
		boolean a = true;
		boolean b = false;

		//String is a class not a data type
		
		String H = "Java Testing";
		
	}
}
